public class IsPrime {

	public static void main(String[] args) {
		
		int a,n=5;
		a=n/2;
	
		
		for (int i = 2; i <=a; i++) {
			
			if(n%i==0) {
				
			System.out.println(n+" is not prime number");
			break;
			
			}
			else
			{
				System.out.println(n+" is prime number");
	}
}
	}}
	


